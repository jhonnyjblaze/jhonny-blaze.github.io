////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
  // if num1 is less than num2 return num1
  if (num1 < num2) { return num1; }
  // otherwise return num2
  else return num2;
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  // if num is even return true
  if (num % 2 === 0) { return true; }
  // otherwise return false
  else return false;
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) { 
  // assign variable named count to 0
  let count = 0;
  // iterate the input string
  for (let i = 0; i < string.length; i++) {
    // check if char input matches character at each index of string
    if (string[i] === char) {
      // if true add one to count
      count++;
    }
  }
  // return count
  return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  // assign variable named count to 0
  let count = 0;
  // iterate the input string
  for (let i = 0; i < string.length; i++) {
    // check if 'B' matches the value at each index of string
    if (string[i] === 'B') {
      // if true add one to count
      count++
    }
  }
  // return count
  return count;
}

