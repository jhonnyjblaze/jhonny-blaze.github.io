function flatten(arr) {
  // return the input arr reduced with params flatArr, currArr
  return arr.reduce((flatArr, currArr) => {
    // return flatArr concatenated with currArr
    return flatArr.concat(currArr);
     // seed Array literal
  }, []);
}

